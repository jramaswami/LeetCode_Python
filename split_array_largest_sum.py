"""
LeetCode :: March 2022 Challenge :: 410. Split Array Largest Sum
jramaswami
"""


class Solution:
    def splitArray(self, nums, m):

        def count_subarrays(target):
            endpoints = []
            curr_sum = 0
            for i, n in enumerate(nums):
                if n + curr_sum > target:
                    endpoints.append(i-1)
                    curr_sum = 0
                curr_sum += n
            if not endpoints or endpoints[-1] != len(nums)-1:
                endpoints.append(len(nums)-1)
            return len(endpoints)

        lo = max(nums)
        hi = sum(nums)
        soln = sum(nums)
        while lo <= hi:
            mid = lo + ((hi - lo) // 2)
            k = count_subarrays(mid)
            if k <= m:
                soln = min(mid, soln)
                hi = mid - 1
            else:
                lo = mid + 1
        return soln


